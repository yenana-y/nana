import { bookshopApi } from "../../api/bookshop";
export default {
  name: "Bookshop",
  data() {
    return {
      firstList: [],
      first1List: [],
      firstpicList: []
    };
  },
  created() {
    this.handleGetFirstList(5);
    this.handleGetFirst1List(5);
    this.handleGetFirstpicList(5);
  },
  watch:{
      firstList(){
        // console.log("更新了")
        this.$refs.scroll.handlefinishPullDown(()=>{
          this.handleGetFirstList(1);
        });
      }
  },
  mounted(){
    this.$refs.scroll.handlepullingDown(()=>{
      this.handleGetFirstList(1);
    });
    this.$refs.scroll.handleScroll();
  },
  methods: {
    ///图片加书名
    async handleGetFirstList(currPage) {
      let data = await bookshopApi(currPage);
      this.firstList = data.result.list[0].data[0].data[0];
      //console.log(this.firstList);
    },
    ///五年老店
    async handleGetFirst1List(currPage) {
      let data = await bookshopApi(currPage);
      this.first1List = data.result.list[0].data[0].data[0].tags;
      //console.log(this.first1List);
    },
    ///图片列
    async handleGetFirstpicList(currPage) {
      let data = await bookshopApi(currPage);
      this.firstpicList = data.result.list[0].data[0].data;
      //console.log(this.firstpicList);
    }
  }
};
